def _branch_prefix_for(pr_id: str) -> str:
    """Return the canonical branch prefix the daemon emits for ``pr_id``.

    Mirrors the formula in :func:`_task_markdown` so cleanup can match only
    branches authored by the current test session.
    """
    return f"pr-{pr_id.lower().replace('.', '-')}-"


def _list_owned_testbed_pr_numbers(
    pr_ids: Iterable[str], *, state: str = "open"
) -> set[str]:
    """Return PR numbers in ``state`` whose head branch belongs to one of ``pr_ids``.

    Filtering by head-branch prefix keeps teardown scoped to PRs created by
    this test session, so a parallel CI run or an unrelated PR opened on
    the testbed during the test is never closed. ``state`` accepts any value
    ``gh pr list --state`` does (``open``, ``merged``, ``closed``, ``all``).
    """
    prefixes = tuple(_branch_prefix_for(pid) for pid in pr_ids)
    if not prefixes:
        return set()
    result = subprocess.run(
        [
            "gh",
            "pr",
            "list",
            "-R",
            TESTBED_REPO,
            "--state",
            state,
            "--limit",
            "1000",
            "--json",
            "number,headRefName",
        ],
        capture_output=True,
        text=True,
        check=False,
    )
    if result.returncode != 0:
        raise RuntimeError(
            f"Failed to list {state} PRs on {TESTBED_REPO} during testbed cleanup "
            f"(exit {result.returncode}): {result.stderr.strip() or result.stdout.strip()}"
        )
    try:
        prs = json.loads(result.stdout or "[]")
    except json.JSONDecodeError as exc:
        raise RuntimeError(
            f"Failed to parse `gh pr list` output during testbed cleanup: {exc}"
        ) from exc
    owned: set[str] = set()
    for pr in prs:
        head = pr.get("headRefName") or ""
        if any(head.startswith(prefix) for prefix in prefixes):
            owned.add(str(pr.get("number")))
    return owned


def _get_testbed_branch_sha(branch: str = TESTBED_DEFAULT_BRANCH) -> str:
    result = subprocess.run(
        [
            "gh",
            "api",
            f"repos/{TESTBED_REPO}/git/ref/heads/{branch}",
            "-q",
            ".object.sha",
        ],
        capture_output=True,
        text=True,
        check=False,
    )
    if result.returncode != 0:
        raise RuntimeError(
            f"Failed to read {branch!r} HEAD on {TESTBED_REPO} "
            f"(exit {result.returncode}): "
            f"{result.stderr.strip() or result.stdout.strip()}"
        )
    sha = result.stdout.strip()
    if not sha:
        raise RuntimeError(
            f"`gh api` returned empty SHA for {branch!r} on {TESTBED_REPO}"
        )
    return sha


def _reset_testbed_branch(sha: str, branch: str = TESTBED_DEFAULT_BRANCH) -> None:
    """Force-reset the testbed default branch to ``sha``.

    Used after a test-owned PR auto-merges so the next run starts from the
    same baseline the fixture captured at entry. Callers MUST verify with
    :func:`_assert_only_owned_commits_on_branch` first so unrelated commits
    that landed concurrently are not silently discarded.
    """
    result = subprocess.run(
        [
            "gh",
            "api",
            "-X",
            "PATCH",
            f"repos/{TESTBED_REPO}/git/refs/heads/{branch}",
            "-f",
            f"sha={sha}",
            "-F",
            "force=true",
        ],
        capture_output=True,
        text=True,
        check=False,
    )
    if result.returncode != 0:
        raise RuntimeError(
            f"Failed to reset {branch!r} to {sha} on {TESTBED_REPO} during testbed "
            f"cleanup (exit {result.returncode}): "
            f"{result.stderr.strip() or result.stdout.strip()}"
        )


def _get_pr_merge_sha(number: str) -> str | None:
    """Return the merge commit SHA for ``number`` on the testbed, or None."""
    result = subprocess.run(
        [
            "gh",
            "pr",
            "view",
            number,
            "-R",
            TESTBED_REPO,
            "--json",
            "mergeCommit",
            "-q",
            ".mergeCommit.oid",
        ],
        capture_output=True,
        text=True,
        check=False,
    )
    if result.returncode != 0:
        raise RuntimeError(
            f"Failed to read merge commit for PR #{number} on {TESTBED_REPO} "
            f"(exit {result.returncode}): "
            f"{result.stderr.strip() or result.stdout.strip()}"
        )
    return result.stdout.strip() or None


def _get_commit_first_parent(sha: str) -> str | None:
    """Return the first-parent SHA of ``sha`` on the testbed, or None at root."""
    result = subprocess.run(
        [
            "gh",
            "api",
            f"repos/{TESTBED_REPO}/commits/{sha}",
            "-q",
            ".parents[0].sha",
        ],
        capture_output=True,
        text=True,
        check=False,
    )
    if result.returncode != 0:
        raise RuntimeError(
            f"Failed to read commit {sha} on {TESTBED_REPO} during testbed cleanup "
            f"(exit {result.returncode}): "
            f"{result.stderr.strip() or result.stdout.strip()}"
        )
    return result.stdout.strip() or None


def _assert_only_owned_commits_on_branch(
    baseline_sha: str,
    current_sha: str,
    owned_merge_shas: set[str],
    *,
    max_walk: int = 100,
) -> None:
    """Walk first-parent from ``current_sha`` to ``baseline_sha``.

    Raises if any commit on the chain is not in ``owned_merge_shas``, so the
    caller can refuse to rewrite the default branch when an unrelated push
    landed alongside the test-owned merges.
    """
    sha: str | None = current_sha
    walked = 0
    while sha and sha != baseline_sha:
        if sha not in owned_merge_shas:
            raise RuntimeError(
                f"Refusing to reset {TESTBED_DEFAULT_BRANCH} on {TESTBED_REPO}: "
                f"commit {sha} on the first-parent chain is not attributable "
                f"to any test-owned merged PR "
                f"(owned merges={sorted(owned_merge_shas)}). Clean up manually."
            )
        sha = _get_commit_first_parent(sha)
        walked += 1
        if walked > max_walk:
            raise RuntimeError(
                f"Walked {max_walk} commits from {current_sha} without reaching "
                f"baseline {baseline_sha} on {TESTBED_REPO}; aborting reset."
            )
    if sha != baseline_sha:
        raise RuntimeError(
            f"First-parent walk from {current_sha} ended at {sha} instead of "
            f"baseline {baseline_sha} on {TESTBED_REPO}; aborting reset."
        )


def _close_testbed_prs(numbers: Iterable[str]) -> None:
    for number in numbers:
        close = subprocess.run(
            ["gh", "pr", "close", number, "-R", TESTBED_REPO, "--delete-branch"],
            capture_output=True,
            text=True,
            check=False,
        )
        if close.returncode != 0:
            raise RuntimeError(
                f"Failed to close PR #{number} on {TESTBED_REPO} during testbed "
                f"cleanup (exit {close.returncode}): "
                f"{close.stderr.strip() or close.stdout.strip()}"
            )


def _wait_daemon_idle(timeout_sec: float = 60.0) -> None:
    deadline = time.monotonic() + timeout_sec
    last: dict[str, Any] | None = None
    while time.monotonic() < deadline:
        try:
            last = _fetch_state(TESTBED_SLUG)
        except requests.RequestException:
            last = None
        else:
            if (
                last is not None
                and last.get("state") == "IDLE"
                and last.get("current_pr") in (None, "", 0)
            ):
                return
        time.sleep(1.0)
    raise TimeoutError(
        f"Daemon did not reach IDLE for slug {TESTBED_SLUG!r} within "
        f"{timeout_sec}s. Last state: {last}"
    )


@pytest.fixture
def reset_testbed_clean() -> Iterable[Callable[[str], None]]:
    """Yield a registration callback; teardown closes only test-owned PRs.

    Tests must call the yielded ``register(pr_id)`` for each task they upload.
    Teardown closes any open PR whose head branch matches a registered
    PR_ID. Because ``config.test.yml`` enables auto-merge, teardown also
    handles registered PRs that merged: it walks first-parent from the
    current default-branch HEAD back to the SHA captured at fixture entry,
    confirms every commit on that chain is the merge commit of a registered
    test-owned PR, and only then force-resets the branch back to baseline.
    If anything unrelated pushed to the default branch in the meantime, the
    walk raises ``RuntimeError`` instead of silently rewriting history.
    """
    registered: set[str] = set()
    baseline_sha = _get_testbed_branch_sha()

    def register(pr_id: str) -> None:
        registered.add(_normalize_pr_id(pr_id))

    yield register
    open_owned = _list_owned_testbed_pr_numbers(registered, state="open")
    _close_testbed_prs(open_owned)
    merged_owned = _list_owned_testbed_pr_numbers(registered, state="merged")
    if merged_owned:
        current_sha = _get_testbed_branch_sha()
        if current_sha != baseline_sha:
            owned_merge_shas = {
                sha
                for number in merged_owned
                if (sha := _get_pr_merge_sha(number)) is not None
            }
            _assert_only_owned_commits_on_branch(
                baseline_sha, current_sha, owned_merge_shas
            )
            _reset_testbed_branch(baseline_sha)
    _wait_daemon_idle()
