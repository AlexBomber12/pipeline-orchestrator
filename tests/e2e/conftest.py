def _task_markdown(pr_id: str, title_slug: str, coder: str, priority: int) -> str:
    branch = f"pr-{pr_id.lower().replace('.', '-')}-{title_slug}"
    return (
        f"# {pr_id}: {title_slug.replace('-', ' ')}\n"
        "\n"
        f"Branch: {branch}\n"
        "- Type: feature\n"
        "- Complexity: low\n"
        "- Depends on: none\n"
        f"- Priority: {priority}\n"
        f"- Coder: {coder}\n"
        "\n"
        "## Problem\n"
        "\n"
        "Synthetic task generated by the e2e test harness.\n"
        "\n"
        "## Scope\n"
        "\n"
        "Touch one placeholder file to validate the pipeline end-to-end.\n"
        "\n"
        "## Files to touch\n"
        "\n"
        "`README.md`: append a single line.\n"
        "\n"
        "## Files NOT to touch\n"
        "\n"
        "Anything outside the touch list.\n"
        "\n"
        "## Success criteria\n"
        "\n"
        "1. README.md is updated and the PR is opened against main.\n"
    )


def _normalize_pr_id(pr_id: str) -> str:
    """Return ``pr_id`` in canonical ``PR-<suffix>`` form.

    Both the dashboard's upload endpoint
    (``^PR-[A-Za-z0-9._-]+\\.md$`` in ``src/web/app.py``) and the daemon's
    task-header parser (``^#\\s+PR-...`` in ``src/queue_parser.py``) require
    the ``PR-`` prefix, so callers that pass a bare numeric (e.g. ``"800"``)
    would otherwise be rejected by either the upload (HTTP 422) or the
    daemon when it tries to read the task file.
    """
    suffix = pr_id[3:] if pr_id[:3].upper() == "PR-" else pr_id
    return f"PR-{suffix}"


